// Define a function named Solve
void Solve(){
    // Declare two double variables, h and m to store hours and minutes respectively
    double h, m;
    // Declare a char variable, dot to store the colon between hours and minutes in the input
    char dot;
    // Read input from the user and store it in h, dot and m
    cin >> h >> dot >> m;
    // Calculate the angle between the hour and minute hands of a clock using the formula |30*h + 0.5*m - 6*m|
    double angle = abs(30 * h + 0.5 * m - 6 * m);
    // If the angle is greater than 180 degrees, then subtract it from 360 to get the smaller angle
    if (angle > 180) angle = 360 - angle;
    // Print the angle with 6 decimal places using cout
    cout << fixed(6) << angle << '\n';
}

